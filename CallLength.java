public class CallLength {

        public static void main(String[] args) {
                int x1, x2, y1, y2;
                double answer;
                x1=3;
                x2=6;
                y1=9;
                y2=4;
                answer = Math.sqrt(Math.pow(x2 - x1, 2) + Math.pow(y2 - y1, 2));
                System.out.println(answer);
        }
}
